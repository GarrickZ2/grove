export function AgentAvatar({ name, size, className, style }: AgentAvatarProps) {
  const icon = getAgentIcon(name, Math.round(size * 0.8));

  if (icon) {
    return (
      <span
        className={className}
        style={{
          width: size,
          height: size,
          borderRadius: '50%',
          display: 'inline-flex',
          alignItems: 'center',
          justifyContent: 'center',
          flexShrink: 0,
          overflow: 'hidden',
          ...style,
        }}
      >
        {icon}
      </span>
    );
  }

  // Fallback: first letter initial
  return (
    <span
      className={className}
      style={{
        width: size,
        height: size,
        borderRadius: '50%',
        background: 'var(--color-highlight)',
        color: 'white',
        display: 'inline-flex',
        alignItems: 'center',
        justifyContent: 'center',
        fontSize: Math.round(size * 0.55),
        fontWeight: 700,
        flexShrink: 0,
        ...style,
      }}
    >
      {name[0]?.toUpperCase() ?? '?'}
    </span>
  );
}

/** Mini version for gutter — returns just the icon or initial, sized for gutter */
export function GutterAvatar({ name }: { name: string }) {
  return <AgentAvatar name={name} size={18} className="diff-gutter-avatar" />;
}
